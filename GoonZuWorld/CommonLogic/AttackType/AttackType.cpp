cltAttackTypeInfoManager::~cltAttackTypeInfoManager()
{
	SI32 i;

	for(i = 0;i < MAX_ATTACKTYPE_NUMBER;i++)
	{
		if(pclAttackTypeInfo[i])
		{
			delete pclAttackTypeInfo[i];
			pclAttackTypeInfo[i] = NULL;
		}
	}

}

